package priyanka;

public class Arithmatic {

	public static void main(String[] args) {
		//add
		int a=10;
		int b=20;
		int c= a+b;
		System.out.println(c);
		int d=40;
		int e=50;
		System.out.println(d+e);
		//create variables
		int p=4;
		int q;
		// value using=
		q = p;
		System.out.println(q);
		// value using=+
		q += p;
		System.out.println(q);
		// value using=*
		q *= p;
		System.out.println(q);
	    //subtraction
		int i=20;
		int j=15;
		int k= i-j;
		System.out.println(k);
		// Multiplication
		int x=6;
		int y=3;
		System.out.println(x*y);
		//Division
		int m=7;
		int n=2;
		System.out.println(m/n);
		//Modulus
		double num1=9;
		double num2=3;
		System.out.println(num1%num2);
		//increment ++
		int g=6;
		int h=++g;
		System.out.println(h++);
		//decrement --
		int num3=6;
		int num4=num3--;
		System.out.println(num4--);
		
		
		

	}

}
